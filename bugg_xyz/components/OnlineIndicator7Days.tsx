const defaultStatuses: SevenDayIndicator = {
  monday: new Array(24).fill(0) as DaysData,
  tuesday: new Array(24).fill(0) as DaysData,
  wednesday: new Array(24).fill(0) as DaysData,
  thursday: new Array(24).fill(0) as DaysData,
  friday: new Array(24).fill(0) as DaysData,
  saturday: new Array(24).fill(0) as DaysData,
  sunday: new Array(24).fill(0) as DaysData,
};

