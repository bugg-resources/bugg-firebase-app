export default function OnlineIndicator7Days(props: { recorder: Recorder }) {
  let loadingBar = useLoadingBar();

  let [status, setStatus] = useState(defaultStatuses);
  let [isLoading, setIsLoading] = useState(false);

  let recorder = props.recorder;

  useEffect(() => {
    // Fetch one audio record for each hour for the last 24 hours

    if (!recorder || !recorder.audioCount) {
      return;
    }

    loadingBar.start();
    setIsLoading(true);

    let monday = new Array(24).fill(0) as DaysData;
    let tuesday = new Array(24).fill(0) as DaysData;
    let wednesday = new Array(24).fill(0) as DaysData;
    let thursday = new Array(24).fill(0) as DaysData;
    let friday = new Array(24).fill(0) as DaysData;
    let saturday = new Array(24).fill(0) as DaysData;
    let sunday = new Array(24).fill(0) as DaysData;

    let weekStatuses = [
      monday,
      tuesday,
      wednesday,
      thursday,
      friday,
      saturday,
      sunday,
    ];

    let baseDate = startOfHour(addHours(new Date(), 1));

    for (let d = 0; d < 7; d++) {
      for (let i = 0; i < 24; i++) {
        let hr = d * 24 + i;
        let dateFrom = subHours(baseDate, hr);

        let hasData = recorder.audioCount[dateFrom.toISOString()] > 0;
        let dayOfWeek = parseInt(format(dateFrom, "i"));
        let hourOfDay = parseInt(format(dateFrom, "H"));
        // @ts-ignore
        weekStatuses[dayOfWeek - 1][hourOfDay] = hasData ? 1 : 0;
      }
    }

    setStatus({
      monday: weekStatuses[0],
      tuesday: weekStatuses[1],
      wednesday: weekStatuses[2],
      thursday: weekStatuses[3],
      friday: weekStatuses[4],
      saturday: weekStatuses[5],
      sunday: weekStatuses[6],
    });
    loadingBar.complete();
    setIsLoading(false);

    return () => {
      setStatus(defaultStatuses);
    };
  }, [setStatus, setIsLoading]);

  return (
    <div className="">
      {isLoading && (
        <div className="flex items-center justify-center h-36 relative">
          <span className="flex h-3 w-3">
            <span className="animate-ping relative inline-flex rounded-full h-3 w-3 bg-borange"></span>
          </span>
        </div>
      )}
      {!isLoading && <Graphic {...status}></Graphic>}
    </div>
  );
}

